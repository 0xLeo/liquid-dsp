unsigned int count_ones_static(unsigned int _x) {
    unsigned int c=0;
#if SIZEOF_INT == 4
    c += c_ones[  _x      & 0xFF ];
    c += c_ones[ (_x>> 8) & 0xFF ];
    c += c_ones[ (_x>>16) & 0xFF ];
    c += c_ones[ (_x>>24) & 0xFF ];
#else
    unsigned int i;
    for (i=0; i<SIZEOF_INT; i++) {
        c += c_ones[ _x & 0xFF ];
        _x >>= 8;
    }   
#endif
    return c;
}

unsigned int count_leading_zeros(unsigned int _x) 
{
    // look for first non-zero byte in _x

    int i, B, clz=0;

    for (i=(SIZEOF_UNSIGNED_INT-1)*8; i>=0; i-=8) {
        B = (_x >> i) & 0xFF;
        if ( B ) // B != 0
            return clz + leading_zeros[B];
        else
            clz += 8;
    }   
    return (SIZEOF_UNSIGNED_INT*8);
}

unsigned int msb_index(unsigned int _x)
{
    unsigned int bits;

#if defined __i386__ || defined __amd64__ || defined __x86_64__
    __asm volatile("bsrl %1,%0\n"
        : "=r" (bits)
        : "c" (_x)
    );
    return bits + 1;
#elif 0
    for (bits = 0; _x != 0 && bits < 32; _x >>= 1, ++bits)
        ;
    return bits;
#else
    unsigned int i, b;
    bits = 0;
    for (i=0; i<SIZEOF_UNSIGNED_INT; i++) {
        b = (_x >> (SIZEOF_UNSIGNED_INT-i-1)*8) & 0xff;
        bits += leading_zeros[b];
        if ( b )
            return 32-bits;
    }
    return 32-bits;
        
#endif
}


