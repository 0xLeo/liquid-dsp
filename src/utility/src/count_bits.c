unsigned int count_ones_static(unsigned int _x) {
    unsigned int c=0;
#if SIZEOF_INT == 4
    c += c_ones[  _x      & 0xFF ];
    c += c_ones[ (_x>> 8) & 0xFF ];
    c += c_ones[ (_x>>16) & 0xFF ];
    c += c_ones[ (_x>>24) & 0xFF ];
#else
    unsigned int i;
    for (i=0; i<SIZEOF_INT; i++) {
        c += c_ones[ _x & 0xFF ];
        _x >>= 8;
    }   
#endif
    return c;
}

